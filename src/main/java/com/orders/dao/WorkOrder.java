package com.orders.dao;

public class WorkOrder {

    private long id;
    private String bphone;
    private String weiXinQun;
    private String wenTiDes;
    private String huiFu;
    private String img;
    private String cTime;
    private String shTime;
    private String shenHe;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getBphone() {
        return bphone;
    }

    public void setBphone(String bphone) {
        this.bphone = bphone;
    }

    public String getWeiXinQun() {
        return weiXinQun;
    }

    public void setWeiXinQun(String weiXinQun) {
        this.weiXinQun = weiXinQun;
    }

    public String getWenTiDes() {
        return wenTiDes;
    }

    public void setWenTiDes(String wenTiDes) {
        this.wenTiDes = wenTiDes;
    }

    public String getHuiFu() {
        return huiFu;
    }

    public void setHuiFu(String huiFu) {
        this.huiFu = huiFu;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getcTime() {
        return cTime;
    }

    public void setcTime(String cTime) {
        this.cTime = cTime;
    }

    public String getShTime() {
        return shTime;
    }

    public void setShTime(String shTime) {
        this.shTime = shTime;
    }

    public String getShenHe() {
        return shenHe;
    }

    public void setShenHe(String shenHe) {
        this.shenHe = shenHe;
    }
}
