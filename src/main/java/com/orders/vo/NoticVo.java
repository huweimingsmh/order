package com.orders.vo;

public class NoticVo {
    private int isP;
    private String title;
    private String cTime;
    private int isR;
    private long id;

    public int getIsP() {
        return isP;
    }

    public void setIsP(int isP) {
        this.isP = isP;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getcTime() {
        return cTime;
    }

    public void setcTime(String cTime) {
        this.cTime = cTime;
    }

    public int getIsR() {
        return isR;
    }

    public void setIsR(int isR) {
        this.isR = isR;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }
}
